package calculator;

public class operations {
    public static int somar(int a, int b){ return a+b; }

    public static double somar(double a, double b){ return (double) a+b; }

    public static int subtr(int a, int b){ return a-b; }

    public static double subtr(double a, double b){ return (double) a-b; }

    public static int multip(int a, int b){ return a*b; }

    public static double multip(double a, double b){ return a*b; }

    public static int div(int a, int b){ return a/b; }

    public static double div(double a, double b){ return a/b; }

}
