package calculator;

public class Operations
{
    public int somar(int a, int b){ return a+b; }

    public double somar(double a, double b){ return (double) a+b; }

    public int subtr(int a, int b){ return a-b; }

    public double subtr(double a, double b){ return (double) a-b; }

    public int multip(int a, int b){ return a*b; }

    public double multip(double a, double b){ return a*b; }

    public int div(int a, int b){ return a/b; }

    public double div(double a, double b){ return a/b; }

}
